package com.digicoffer.lauditor.TimeSheets.Models;

public class DateModel {
    private String date;
    private String day;

    public DateModel(String date) {
        this.date = date;
        this.day = day;
    }

    public String getDate() {
        return date;
    }

    public String getDay() {
        return day;
    }
}
